window.addEventListener('DOMContentLoaded', () => {
  const egStockIndicatorBar = document.querySelector('#sp-stock-indicator-bar');

  if (egStockIndicatorBar) {
    egStockIndicatorBar.style.display = 'block';
    document.querySelector('button[name="add"][id="productMainSubmit"]').insertAdjacentElement('beforebegin', egStockIndicatorBar);

    const initWidth = () => {
      setTimeout(() => {
        const progressBar = document.querySelector('#sp-stock-indicator-bar .progress-bar');
        if (progressBar) {
          progressBar.style.width = progressBar.dataset.width;
        }
      }, 500);
    };
    initWidth();

    document.querySelectorAll('input[name="id"]').forEach((input) => {
      input.addEventListener('change', async () => {
        const innerBar = document.querySelector('#sp-stock-indicator-bar-inner');
        if (innerBar) {
          innerBar.style.opacity = '0.25';

          const response = await fetch(window.location.href);
          const data = await response.text();

          const parser = new DOMParser();
          const newDocument = parser.parseFromString(data, 'text/html');

          const newInnerBar = newDocument.querySelector('#sp-stock-indicator-bar-inner');
          if (newInnerBar) {
            innerBar.replaceWith(newInnerBar);
            initWidth();
          }
        }
      });
    });
  }
});



